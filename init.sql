CREATE DATABASE IF NOT EXISTS phone_shop;
USE phone_shop;
CREATE TABLE IF NOT EXISTS customers (
    customers_id INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) UNIQUE NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL,
    is_active BOOLEAN DEFAULT TRUE,
    is_staff BOOLEAN DEFAULT FALSE,
    is_superuser BOOLEAN DEFAULT FALSE,
    last_login DATETIME,
    tel VARCHAR(20),
    date_joined DATETIME DEFAULT CURRENT_TIMESTAMP,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    address TEXT
);
CREATE TABLE IF NOT EXISTS customers_user_permissions (
    id INT AUTO_INCREMENT PRIMARY KEY,
    customer_id INT,
    permission_id INT,
    FOREIGN KEY (customer_id) REFERENCES customers(customers_id)
);
CREATE TABLE IF NOT EXISTS phone_brands (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    image_url VARCHAR(255)
);
CREATE TABLE IF NOT EXISTS product (
    product_id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    price DECIMAL(10, 2) NOT NULL,
    camera VARCHAR(100),
    ram VARCHAR(50),
    rom VARCHAR(50),
    screen VARCHAR(100),
    image VARCHAR(255),
    battery VARCHAR(50),
    brand_id INT,
    cpu VARCHAR(100),
    description TEXT,
    origin VARCHAR(50),
    os VARCHAR(50),
    year INT,
    FOREIGN KEY (brand_id) REFERENCES phone_brands(id)
);
CREATE TABLE IF NOT EXISTS orders (
    order_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_id INT,
    date DATETIME DEFAULT CURRENT_TIMESTAMP,
    status VARCHAR(20),
    address TEXT,
    FOREIGN KEY (customer_id) REFERENCES customers(customers_id)
);
CREATE TABLE IF NOT EXISTS cart (
    cart_id INT AUTO_INCREMENT PRIMARY KEY,
    product_id INT,
    quantity INT,
    price DECIMAL(10, 2),
    customers_id INT,
    FOREIGN KEY (product_id) REFERENCES product(product_id),
    FOREIGN KEY (customers_id) REFERENCES customers(customers_id)
);
CREATE TABLE IF NOT EXISTS order_items (
    order_item_id INT AUTO_INCREMENT PRIMARY KEY,
    product_id INT,
    quantity INT,
    price DECIMAL(10, 2),
    order_id INT,
    FOREIGN KEY (product_id) REFERENCES product(product_id),
    FOREIGN KEY (order_id) REFERENCES orders(order_id)
);
